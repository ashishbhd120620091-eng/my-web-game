export const OutfitManager = {
  loadData() {
    try {
      const saved = localStorage.getItem('game_outfit_data');
      if (saved) return JSON.parse(saved);
    } catch (e) {}
    
    return {
      unlocked: ['h1', 's1', 'p1', 'sh1'], // Defaults
      equipped: { Hair: 'h1', Cap: null, Mask: null, Shirt: 's1', Pant: 'p1', Shoes: 'sh1' },
      coins: parseInt(localStorage.getItem('game_coins') || '1000', 10)
    };
  },

  saveData(data) {
    localStorage.setItem('game_outfit_data', JSON.stringify(data));
    localStorage.setItem('game_equipped_outfits', JSON.stringify(data.equipped));
  },

  purchase(itemId) {
    const item = ITEMS.find(i => i.id === itemId);
    if (!item) return false;
    
    const data = this.loadData();
    if (data.unlocked.includes(itemId)) return true;
    
    if (data.coins >= item.price) {
      data.coins -= item.price;
      data.unlocked.push(itemId);
      this.saveData(data);
      // Sync coins with main game
      localStorage.setItem('game_coins', String(data.coins));
      return true;
    }
    return false;
  },

  equip(category, itemId) {
    const data = this.loadData();
    data.equipped[category] = itemId;
    this.saveData(data);
    return data;
  }
};
